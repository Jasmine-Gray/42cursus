#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new;
	t_list	*list_cpy;

	list_cpy = NULL;
	if (!lst || !f || !del)
		return (NULL);
	while (lst)
	{
		new = ft_lstnew(f(lst->content));
		if (!new)
		{
			ft_lstclear(&list_cpy, del);
			return (NULL);
		}
		ft_lstadd_back(&list_cpy, new);
		lst = lst->next;
	}
	return (list_cpy);
}
